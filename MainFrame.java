    private void scheduleClassSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_scheduleClassSubmitButtonActionPerformed
        // TODO add your handling code here:
        String status = "";
        String studentOption = (String)scheduleClassStudentComboBox.getSelectedItem();
        String[] studentInfo = studentOption.split(" ");
        String lastFirst = studentInfo[2] + " " + studentInfo[3];
        for(StudentEntry s : StudentQueries.getAllStudents()){
            if(s.getStudentID().equals(studentInfo[0])){
                currentStudent = new StudentEntry(s.getStudentID(),s.getFirstName(),s.getLastName());
            }
        }
        String courseCode = (String)scheduleClassSelectComboBox.getSelectedItem();
        if(ScheduleQueries.checkDuplicates(currentSemester, courseCode, currentStudent.getStudentID())){
            scheduleClassStatusLabel.setText(lastFirst + " has already enrolled in " + courseCode);
        }else{
            if (ClassQueries.getClassSeats(currentSemester, courseCode) > ScheduleQueries.getScheduledStudentCount(currentSemester, courseCode)){
                status = "Scheduled";
                scheduleClassStatusLabel.setText(lastFirst + " has been scheduled for " + courseCode);
            }
            else{
                status = "Waitlisted";
                scheduleClassStatusLabel.setText(lastFirst + " has been waitlisted for " + courseCode);
            }
            ScheduleEntry newSchedule = new ScheduleEntry(currentSemester, courseCode, currentStudent.getStudentID(), status);
            ScheduleQueries.addScheduleEntry(newSchedule);
        }    
    }//GEN-LAST:event_scheduleClassSubmitButtonActionPerformed

