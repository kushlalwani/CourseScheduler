    private void dropStudentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropStudentButtonActionPerformed
        // TODO add your handling code here:
    String studentInfo = (String) dropStudentComboBox.getSelectedItem();
    StudentEntry currStudent = StudentQueries.getStudent(studentInfo.split(" ")[0]);
    dropStudentTextArea.append(studentInfo + " has been dropped from the list of students.\n\n");

    for (String currSem : SemesterQueries.getSemesterList()) {
        String droppedClasses = "";
        String scheduledClasses = "";

        for (ScheduleEntry currentEntry : ScheduleQueries.getScheduleByStudent(currSem, currStudent.getStudentID())) {
            ScheduleQueries.dropStudentScheduleByCourse(currSem, currStudent.getStudentID(), currentEntry.getCourseCode());

            String dropMessage;
            if (currentEntry.getStatus().equals("Waitlisted")) {
                dropMessage = " has been dropped from the waitlist for ";
            } else {
                dropMessage = " has been dropped from ";
            }

            droppedClasses += studentInfo + dropMessage + currentEntry.getCourseCode() + "\n";

            ArrayList<ScheduleEntry> waitlistedStudents = ScheduleQueries.getWaitlistedStudentsByCourse(currSem, currentEntry.getCourseCode());
            
            if (!waitlistedStudents.isEmpty()) {
                ScheduleEntry waitlistedEntry = waitlistedStudents.get(0);
                ScheduleQueries.updateScheduleEntry(waitlistedEntry);
                StudentEntry movedStudent = StudentQueries.getStudent(waitlistedEntry.getStudentID());
                scheduledClasses += movedStudent.getStudentID() + " - " + movedStudent.getLastName() + ", " + movedStudent.getFirstName() + " has been scheduled into " + waitlistedEntry.getCourseCode() + "\n";
            }
        }

        if (!droppedClasses.isEmpty() || !scheduledClasses.isEmpty()) {
            dropStudentTextArea.append("For Semester: " + currSem + "\n");
            dropStudentTextArea.append(droppedClasses);
            dropStudentTextArea.append(scheduledClasses);
        }
    }

    StudentQueries.dropStudent(studentInfo.split(" ")[0]);
    rebuildStudentComboBoxes();
    clearTable();
    }//GEN-LAST:event_dropStudentButtonActionPerformed

