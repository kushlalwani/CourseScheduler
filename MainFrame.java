    public void rebuildSemesterComboBoxes() {
        ArrayList<String> semesters = SemesterQueries.getSemesterList();
        currentSemesterComboBox.setModel(new javax.swing.DefaultComboBoxModel(semesters.toArray()));
        if (semesters.size() > 0) {
            currentSemesterLabel.setText(semesters.get(0));
            currentSemester = semesters.get(0);
        } else {
            currentSemesterLabel.setText("None, add a semester.");
            currentSemester = "None";
        }
    }
    
    public void rebuildCourseComboBoxes(){
        //creates a new arraylist with the names of all the courses offered during the semester
        ArrayList<String> courseCodes = CourseQueries.getAllCourseCodes();
        addClassCourseCodeComboBox.setModel(new javax.swing.DefaultComboBoxModel(courseCodes.toArray()));
    }
    
    public void rebuildClassComboBoxes(){
        ArrayList<String> courseCodes = ClassQueries.getAllCourseCodes(currentSemester);
        scheduleClassSelectComboBox.setModel(new javax.swing.DefaultComboBoxModel(courseCodes.toArray()));
        displayClassListComboBox.setModel(new javax.swing.DefaultComboBoxModel(courseCodes.toArray()));
        dropClassAdminComboBox.setModel(new javax.swing.DefaultComboBoxModel(courseCodes.toArray()));
        dropClassComboBox.setModel(new javax.swing.DefaultComboBoxModel(courseCodes.toArray()));
    }
    
    public void rebuildStudentComboBoxes(){
        ArrayList<String> names = new ArrayList<>();
        for (StudentEntry s: StudentQueries.getAllStudents()){
            names.add(s.getStudentID() + " - " + s.getLastName()+", "+s.getFirstName());
        }
        scheduleClassStudentComboBox.setModel(new javax.swing.DefaultComboBoxModel(names.toArray()));
        displayScheduleStudentComboBox.setModel(new javax.swing.DefaultComboBoxModel(names.toArray()));
        dropStudentComboBox.setModel(new javax.swing.DefaultComboBoxModel(names.toArray()));
        dropClassStudentComboBox.setModel(new javax.swing.DefaultComboBoxModel(names.toArray()));
    }

    
